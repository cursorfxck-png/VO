-- 6. FIX EXISTING USERS
-- ============================================

INSERT INTO public.profiles (id, email, username, full_name, avatar_url)
SELECT 
    u.id,
    u.email,
    COALESCE(u.raw_user_meta_data->>'username', SPLIT_PART(u.email, '@', 1), 'user_' || SUBSTRING(u.id::text, 1, 8)) as username,
    COALESCE(u.raw_user_meta_data->>'full_name', SPLIT_PART(u.email, '@', 1), 'User') as full_name,
    COALESCE(u.raw_user_meta_data->>'avatar_url', '/download.png') as avatar_url
FROM auth.users u
WHERE u.id NOT IN (SELECT id FROM public.profiles)
ON CONFLICT (id) DO NOTHING;

UPDATE public.profiles
SET username = COALESCE(NULLIF(username, ''), SPLIT_PART(email, '@', 1), 'user_' || SUBSTRING(id::text, 1, 8))
WHERE username IS NULL OR username = '';

UPDATE public.profiles
SET full_name = COALESCE(NULLIF(full_name, ''), SPLIT_PART(email, '@', 1), 'User')
WHERE full_name IS NULL OR full_name = '';

UPDATE public.profiles
SET avatar_url = '/download.png'
WHERE avatar_url IS NULL OR avatar_url = '';

