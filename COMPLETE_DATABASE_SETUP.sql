-- Auto-generate slug for posts (unique URL per post)
CREATE OR REPLACE FUNCTION public.generate_post_slug()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.slug IS NULL OR NEW.slug = '' THEN
        NEW.slug := 'post-' || NEW.id::text || '-' || EXTRACT(EPOCH FROM NEW.created_at)::bigint::text;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS set_post_slug ON public.posts;
CREATE TRIGGER set_post_slug
    BEFORE INSERT ON public.posts
    FOR EACH ROW EXECUTE FUNCTION public.generate_post_slug();

